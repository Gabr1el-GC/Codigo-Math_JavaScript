var numero = 15;

if (numero > 0) { //se o número for maior que 0, o console vai exibir a mensagem abaixo.
    console.log(numero," é positivo.");
} else if (numero < 0) { //caso o número não for maior que 0, então será verificado se ele é menor que 0.
    console.log(numero," é negativo.")
} else { //se não for nenhum dessas condições, o console irá exibir a mensagem abaixo.
    console.log(numero," é zero.")
}
